package practice221227;

public class As {
	public static void main(String[] args) {
		
		String name ="종현이는";
				
		for (int i=7; i<22; i++) {
		System.out.println("[현재 시각: " + i + "시]");
		
		switch (i) {
		case 7:
			System.out.println(name + i +"시에 기상합니다.");
		case 8:
			System.out.println(name + i +"시에 태영이랑 밥먹고 출근합니다.");
		case 9:
			System.out.println(name + i +"시에 졸린눈으로 회사에 도착했습니다.");
		case 10:
			System.out.println(name + i +"시에 개발에 막혀 구글링을 합니다.");
		case 11:
			System.out.println(name + i +"시에 동영이한테 카톡이 와서 짜증납니다.");
		case 12:
			System.out.println(name + i +"시에 미안했는지 동영이한테 카톡한번 날리며 식사합니다.");
		case 13:
			System.out.println(name + i +"시에 식사시간이 끝나고 커피한잔합니다.");
		case 14:
			System.out.println(name + i +"시에 열심히 코딩을합니다.");
		case 15:
			System.out.println(name + i +"시에 어떻게 하면 동영이를 잘 알려줄까 고민을 해봅니다.");
		case 16:
			System.out.println(name + i +"시에 이번에 개발하면서 어떻게 해야 더 잘할까 고민해봅니다.");
		case 17:
			System.out.println(name + i +"시에 퇴근시간이 1시간 남았다는거에 신이납니다.");
		case 18:
			System.out.println(name + i +"시에 칼퇴를 합니다.");
		case 19:
			System.out.println(name + i +"시에 온수역에서 57번 버스를 타고 집으로 갑니다.");
		case 20:
			System.out.println(name + i +"시에 가족과 저녁을 먹으면서 동영이를 가르켜주러 가야되나 말아야 되나 고민합니다.");
		case 21:
			System.out.println(name + i +"시에 태영이가 잠이 들고 동영이 보러 삼성상가 무인카페에 갑니다.");
			}
		}
	}
}