package for221223;

public class Test152ForSumFrom1To100Example {
	public static void main(String[] args) {
		int sum = 0;//---------------------------합계변수
					//								|
		int i = 0;	//----------루프카운터변수			|
		for(i=1; i<=100; i++) {//  |				|
			sum +=1;			// |				|
		}						// |				|
								// |				|
		System.out.println("1~" + (i-1) + "합 : " + sum);
	}
}
 